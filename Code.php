<p>For centuries, scientists have looked up at the night sky and marveled at the luminous matter that seemed to make up our universe. However, in the past century, experiments and observations have revealed that there must be something else out there that we cannot see. The existence of this mysterious substance, known as dark matter, has been theorized to explain the discrepancies in our current understanding of the universe.</p>
