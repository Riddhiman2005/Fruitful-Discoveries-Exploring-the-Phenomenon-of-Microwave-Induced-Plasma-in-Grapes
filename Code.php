<p class="has-large-font-size"><strong><span style="text-decoration: underline">Background</span></strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Everything we touch, smell, taste,eat or see is composed of the same sort of atoms as the thousands of billions of stars that make up the Milky Way Galaxy in which we live. This same sort of atoms make up the stars, dusts and gases that compose the hundreds of billions of other galaxies in our visible Universe, or also the the gas which lies in the intergalactic depths.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Despite the fact that everything we experience in our daily lives is made up of the same sort of atoms, it's important to remember that these atoms are just a fraction of the stuff that makes up the universe. In fact, if we were to add up all the matter and energy in the universe, the sum total of everything we can see, from the tiniest speck of dust to the largest galaxy, would only account for less than 5% of the total. The vast majority of the universe is composed of something entirely different from the matter that makes up our world.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It's a humbling realization that the stuff that we're made of is such a small part of the universe as a whole. But it also highlights just how much we have yet to learn about the cosmos. The study of <strong>dark matter</strong> and <strong>dark energy</strong> is just one example of the ongoing quest to understand the mysteries of the universe. As we continue to explore and push the boundaries of what we know, it's exciting to imagine what other discoveries and revelations may be waiting for us in the vast, unexplored depths of the cosmos.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Despite its invisibility as dark matter doesn't emit light , we know that it exists because of the gravitational effects it has on other bodies in the universe, such as stars and galaxies.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Scientists have long been fascinated by the phenomenon of dark matter, which was first hypothesized to account for discrepancies between the observed mass of galaxies and the mass that could be accounted for by the visible matter they contained. Over time, more and more evidence has accumulated that supports the existence of dark matter, and it is now widely accepted as a fact of the universe.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The most popular theory to explain dark matter is that it is composed of a new type of particle known as weakly interacting <strong>massive particles(WIMPs)</strong> [<em>We will discuss this article in details in our upcoming posts</em>] for short. These particles are thought to interact only through gravity and the weak force, which makes them incredibly difficult to detect or observe directly.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>While there are alternative theories that have been proposed to explain dark matter, there is still much that we don't know about this elusive substance</p>
