<p>Dark Energy:</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Dark energy is a form of energy that is thought to be causing the universe to expand at an accelerating rate. This was first discovered in the late 1990s by studying distant supernovae. </p>
