<p class="has-large-font-size"><strong><span style="text-decoration: underline">FIRST DETECTION OF DARK MATTER</span></strong></p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":713,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="https://everlastingquestforphysics.files.wordpress.com/2023/05/image-12.png?w=404" alt="" class="wp-image-713"/></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>The first detection of dark matter is attributed to Fritz Zwicky (1933), who<br>measured the velocity dispersion of galaxies in the Coma cluster and found<br>their velocities to far exceed that which could be attributed to the luminous<br>matter in the galaxies themselves. <br>He looked at velocity measurements of several individual galaxies in the<br>Coma cluster and noticed that the galaxies seemed to be moving too fast for<br>the amount of visible matter in the cluster. If one adds up the mass in the<br>cluster by assuming that every galaxy has a mass-to-light ratio of about one<br>in solar units, then there isn’t enough mass to hold the cluster together, i.e.,<br>the galaxies should fly apart.<br>Zwicky estimated the cluster's total mass based on the motions of galaxies<br>near its edge (using the virial theorem) and compared that estimate to one<br>based on the number of galaxies and total brightness of the cluster. He found<br>that there was about 400 times more estimated mass than was visually<br>observable. The gravity of the visible galaxies in the cluster would be far too<br>small for such fast orbits, so something extra was required. This is known as<br>the "missing mass problem". Based on these conclusions, Zwicky inferred<br>that there must be some non-visible form of matter which would provide<br>enough of the mass and gravity to hold the cluster together</p>
