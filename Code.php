<!-- wp:paragraph -->
<p>The image above shows the rotation curve of a galaxy. The blue line shows the observed velocity of stars in the galaxy, while the red line shows the predicted velocity based on the visible matter in the galaxy. The fact that the observed velocity is higher than the predicted velocity suggests that there is more matter in the galaxy than we can see, and that matter is dark matter.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Dark matter is thought to make up around 27% of the total mass-energy of the universe. It is believed to be composed of particles that are much heavier than electrons, but much lighter than atoms. These particles are collectively known as Weakly Interacting Massive Particles (WIMPs).</p>
